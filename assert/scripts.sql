CREATE TABLE TBL_NOTA_FISCAL_SERVICO (
	ID BIGINT(20) NOT NULL AUTO_INCREMENT,
	NUMERO BIGINT(20) NOT NULL, 
	DISCRIMINACAO_GERAL VARCHAR(100) NOT NULL,
	DATA DATETIME NOT NULL,
	VALOR DECIMAL(13, 2) NOT NULL,
	PRIMARY KEY (ID)
)ENGINE = innoDB;

