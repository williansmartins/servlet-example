CREATE TABLE `TBL_CLIENTE_NOTA_FISCAL_SERVICO` (
  `CLIENTE_ID` BIGINT NOT NULL ,
  `NFS_ID` BIGINT NOT NULL ,
  PRIMARY KEY (`CLIENTE_ID`, `NFS_ID`) ,
  INDEX `FK_CLINFS_CLI` (`CLIENTE_ID` ASC) ,
  INDEX `FK_CLINFS_NFS` (`NFS_ID` ASC) ,
  CONSTRAINT `FK_CLINFS_CLI`
    FOREIGN KEY (`CLIENTE_ID`)
    REFERENCES `TBL_CLIENTE` (`ID`)
    ON DELETE NO ACTION
    ON UPDATE NO ACTION,
  CONSTRAINT `FK_CLINFS_NFS`
    FOREIGN KEY (`NFS_ID`)
    REFERENCES `TBL_NOTA_FISCAL_SERVICO` (`ID`)
    ON DELETE NO ACTION
    ON UPDATE NO ACTION
)ENGINE = InnoDB;